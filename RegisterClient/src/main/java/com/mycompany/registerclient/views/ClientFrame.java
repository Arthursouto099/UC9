    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String name = this.nameField.getText();
        String email = this.emailField.getText();
        char[] password = this.passwordField.getPassword();

        String cep = this.cepField.getText();
        String estado = this.estadoField.getText();
        String bairro = this.bairroField.getText();
        String logradouro = this.logradouroField.getText();
        String localidade = this.localidadeField.getText();

        String code = AddressController.register(cep, estado, bairro, logradouro, localidade);

        JOptionPane.showMessageDialog(this, code.split(":")[1]);

        String coded = code.split(":")[0];
        System.out.println(coded);
        Address address = AddressController.getByCode(code.split(":")[0]);

        String user = ClientController.register(address.getId(), name, email, password);
        JOptionPane.showMessageDialog(this, user);
        clearReset();


    }//GEN-LAST:event_registerButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String cep = this.cepField.getText();
        try {
            Address address = AddressController.getAddressByCEP(cep);
            String estado = address.getEstado();
            String bairro = address.getBairro();
            String logradouro = address.getLogradouro();
            String localidade = address.getLocalidade();

            this.estadoField.setText(estado);
            this.bairroField.setText(bairro);
            this.logradouroField.setText(logradouro);
            this.localidadeField.setText(localidade);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.painel_2.setVisible(true);
        model.setRowCount(0);

        ArrayList<Client> clients = ClientController.findClients();

        for (Client c : clients) {
            model.addRow(new Object[]{c.getId(), c.getName(), c.getEmail(), c.getAddress()});
        }

        table_users.setModel(model);


    }//GEN-LAST:event_jButton2ActionPerformed

    private void table_usersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_usersMouseClicked
        int index = table_users.getSelectedRow();
        if (index != -1) {
            String email = (String) model.getValueAt(index, 2);
            String name = (String) model.getValueAt(index, 1);
            int id = (int) model.getValueAt(index, 0);

            Client c = new Client(email, name);
            c.setId(id);
            clientSelected = c;

            this.SelectedLabel.setText("1 Usuario selecionado");
        }
    }//GEN-LAST:event_table_usersMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (SelectedLabel.getText() instanceof String) {
            int n = Integer.parseInt(SelectedLabel.getText().split(" ")[0]);
            if (n == 1) {
                JOptionPane.showMessageDialog(this, "Importando informações");
                Address a = AddressController.getAddressByIdClient(clientSelected.getId());

                this.nameField.setText(clientSelected.getName());
                this.emailField.setText(clientSelected.getEmail());

                String id = Integer.toString(clientSelected.getId());

                this.idField.setText(id);

                this.cepField.setText(a.getCep());
                this.estadoField.setText(a.getEstado());
                this.bairroField.setText(a.getBairro());
                this.logradouroField.setText(a.getLogradouro());
                this.localidadeField.setText(a.getLocalidade());
                this.editButton.setEnabled(true);
                this.registerButton.setEnabled(false);
                this.deleteButton.setEnabled(true);
                this.code_address.setText(a.getCode_address());
                this.painel_2.setVisible(false);

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

