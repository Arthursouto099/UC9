    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String name = this.nameField.getText();
        String email = this.emailField.getText();
        char[] password_new = this.passwordField.getPassword();

        String code_address = this.code_address.getText();

        String cep = this.cepField.getText();
        String estado = this.estadoField.getText();
        String bairro = this.bairroField.getText();
        String logradouro = this.logradouroField.getText();
        String localidade = this.localidadeField.getText();

        String password = JOptionPane.showInputDialog(this, "Digite a senha atual do usuario para continuar");
        if (ClientController.login(clientSelected.getEmail(), password)) {
            if (AddressController.editAddress(code_address, cep, estado, bairro, logradouro, localidade)) {
                JOptionPane.showMessageDialog(this, "Endereço editado com sucesso");
                JOptionPane.showMessageDialog(this, ClientController.editClient(Integer.parseInt(idField.getText()), name, email, password_new));
                clearReset();

            };
        } else {
            JOptionPane.showMessageDialog(this, "SENHA NÃO CORRETA");
        }

    }//GEN-LAST:event_editButtonActionPerformed

